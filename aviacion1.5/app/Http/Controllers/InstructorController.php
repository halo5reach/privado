     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(InstructorRequest $request)
    {
          $Instructor = new Instructor();
        $Instructor->nombre = $request->nombre;
        $Instructor->cedula = $request->cedula;     
        $Instructor->telefono = $request->telefono;       
        $Instructor->email = $request->correo;
        
        
        if($Instructor->save()){
            flash('el Instructor '.$Instructor->nombre.' Se ah registrado correctamente', 'success');
        }else{
            flash('ah ocurrrido un error', 'danger');
        }

       
        return redirect()->route('instructor.create');
    }

